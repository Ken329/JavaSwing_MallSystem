    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
        // TODO add your handling code here:
        DecimalFormat df = new DecimalFormat("#####.00");
        double my_money = Double.parseDouble(money.getText());
        double my_changes = my_ammount - my_money;
        if(my_changes < 1){
            change.setVisible(true);
            my_changes = Math.abs(my_changes);
            changes.setText(String.valueOf(df.format(my_changes)));
            try {
                Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/mall", "root", "root");
                Statement st = conn.createStatement();
                st.executeUpdate("update payment set money_insert = '"+df.format(my_money)+"', changes = '"+df.format(my_changes)+"' where sales = '"+my_sales+"'");
                int a = JOptionPane.showConfirmDialog(null, "Thank you so much, do you need the receipt?", "Select", JOptionPane.YES_NO_OPTION);
                if(a == 0){
                    receipt_page rp = new receipt_page();
                    rp.setVisible(true);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Thank you for coming have a nice day!!!");
                    setVisible(false);
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(payment_page.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Money that you have inserted is not enough, Please check and try again!!!");
            money.setText("");
        }
    }//GEN-LAST:event_enterActionPerformed

