    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown

        try {
            // TODO add your handling code here:
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/mall", "root", "root");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select * from staff_detail where active = 'on'");
            while(rs.next()){
                name = rs.getString(1);
                break;
            }
            ResultSet sales = st.executeQuery("select max(sales) from payment where staff = '"+name+"'");
            while(sales.next()){
                my_sales = sales.getString(1);
                break;
            }
            ResultSet total = st.executeQuery("select * from payment where sales = '"+my_sales+"'");
            while(total.next()){
                ammount.setText(total.getString(2));
                my_ammount = Double.parseDouble(total.getString(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(payment_page.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formComponentShown

